package com.itheima.operator;

public class OperatorDemo2 {
    /*
        关系运算符 (比较运算符) :

            >
            >=
            <
            <=
            ==
            !=

        总结: 让数据之间做比较的符号, 返回true, false
     */
    public static void main(String[] args) {
        System.out.println(10 > 20);        // false
        System.out.println(10 >= 20);       // false
        System.out.println(10 < 20);        // true
        System.out.println(10 <= 20);       // true
        System.out.println(10 == 20);       // false
        System.out.println(10 != 20);       // true
    }
}
